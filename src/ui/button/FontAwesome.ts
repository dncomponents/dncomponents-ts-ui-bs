    public static BASE_STYLE: string = 'fa';
    public st: string = null;

    constructor() ;
    constructor(style: string) ;
    constructor(style?: string) {
        super(() => this.st.toString().toLowerCase());
        if (style)
            this.st = style;
    }

    public getStyle(): string {
        return FontAwesome.BASE_STYLE + ' ' + FontAwesome.BASE_STYLE + '-' + this.replace(this.styleCmd());
    }

    public replace(str: string): string {
        let result = '';
        for (let i = 0; i < str.length; i++) {
            if (str.charAt(i) == '_') {
                result += '-';
            } else {
                result += str.charAt(i);
            }
        }
        return result;
    }

    public getIcon(): HTMLElement {
        let i = Util.createI();
        i.className = this.getStyle();
        return i;
    }

    private static fontAwesomeMap: Map<string, FontAwesome>;

    public static get(key: string): FontAwesome {
        if (this.fontAwesomeMap == null) {
            this.fontAwesomeMap = new Map<string, FontAwesome>();
            let keys = Object.keys(this);
            let values = Object.values(this);
            for (let i = 0; i < keys.length; i++) {
                let key = keys[i];
                let val: FontAwesome = values[i];
                if (!(val instanceof FontAwesome))
                    continue;
                if (val.st == null)
                    val.st = key;
                this.fontAwesomeMap.set(keys[i], values[i]);
            }
        }
        return this.fontAwesomeMap.get(key);
    }

